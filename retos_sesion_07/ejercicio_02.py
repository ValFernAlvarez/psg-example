'''
2. Escribe un programa que reciba tres enteros por teclado y reste su resultado
'''
numero_1=int(input("Ingrese el primer entero: "))
numero_2=int(input("Ingrese el segundo entero: "))
numero_3=int(input("Ingrese el tercer entero: "))
#---
resultado=numero_1-numero_2-numero_3
#---
print(f"El resultado de la resta:  {numero_1}-{numero_2}-{numero_3} = {resultado}")